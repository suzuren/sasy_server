CREATE  PROCEDURE `sp_load_match_option`(
IN inServerID int(11))
    SQL SECURITY INVOKER
THIS_PROCEDURE:BEGIN
	DECLARE varI INT DEFAULT 1;
	DECLARE varAwardCnt INT DEFAULT 0;

	DECLARE varServerID INT;
	DECLARE varMatchName VARCHAR(255);
	DECLARE varMatchStartHour TINYINT;
	DECLARE varMatchStartMinute TINYINT;
	DECLARE varMatchEndHour TINYINT;

	DECLARE varMatchEndMinute TINYINT;
	DECLARE varFirstMatchHour TINYINT;
	DECLARE varFirstMatchMinute TINYINT;
	DECLARE varMatchDuration SMALLINT;
	DECLARE varMatchInterval SMALLINT;
	DECLARE varMatchFee BIGINT;
	DECLARE varMatchInitScore BIGINT;
	DECLARE varMatchAwardMinScore BIGINT;
	DECLARE varMatchInitTable INT;

	DECLARE varRank TINYINT;
	DECLARE varGold BIGINT;
	DECLARE varMedal BIGINT;
	DECLARE varExp BIGINT;

	DECLARE varText TEXT;
	
	DECLARE varIsDone TINYINT DEFAULT 0;
	DECLARE varCursor1 CURSOR FOR SELECT `Rank`, `Gold`, `Medal`, `Exp` FROM `MatchOptionAward` WHERE `ServerID`=inServerID ORDER BY `Rank` ASC;
	DECLARE CONTINUE HANDLER FOR NOT FOUND SET varIsDone = 1;

	SELECT `ServerID`, `MatchName`, `MatchStartHour`, `MatchStartMinute`, `MatchEndHour`, `MatchEndMinute`, `FirstMatchHour`, `FirstMatchMinute`, `MatchDuration`, `MatchInterval`, `MatchFee`, `MatchInitScore`, `MatchAwardMinScore`, `MatchInitTable` INTO varServerID, varMatchName, varMatchStartHour, varMatchStartMinute, varMatchEndHour, varMatchEndMinute, varFirstMatchHour, varFirstMatchMinute, varMatchDuration, varMatchInterval, varMatchFee, varMatchInitScore, varMatchAwardMinScore, varMatchInitTable FROM `MatchOption` WHERE `ServerID`=inServerID;
	IF ISNULL(varServerID) THEN
		LEAVE THIS_PROCEDURE;
	END IF;

	SELECT COUNT(Rank) INTO varAwardCnt FROM MatchOptionAward WHERE ServerID=inServerID;
	IF varAwardCnt=0 THEN
		LEAVE THIS_PROCEDURE;
	END IF;


	SET varI := 1;
	DROP TEMPORARY TABLE IF EXISTS `tmp_matchOption`;
	SET @sql := "CREATE TEMPORARY TABLE `tmp_matchOption` (";
	WHILE varI <= varAwardCnt DO
		SET @sql := CONCAT(@sql,"`Rank", varI,"` tinyint(3) unsigned NOT NULL DEFAULT '0',");
		SET @sql := CONCAT(@sql,"`Gold", varI,"` bigint(20) unsigned NOT NULL DEFAULT '0',");
		SET @sql := CONCAT(@sql,"`Medal", varI,"` bigint(20) unsigned NOT NULL DEFAULT '0',");
		
		IF varI=varAwardCnt THEN
			SET @sql := CONCAT(@sql,"`Exp", varI,"` bigint(20) unsigned NOT NULL DEFAULT '0'");
		ELSE
			SET @sql := CONCAT(@sql,"`Exp", varI,"` bigint(20) unsigned NOT NULL DEFAULT '0',");
		END IF;
		
		SET varI := varI + 1;
	END WHILE;
	SET @sql := CONCAT(@sql,")");

	PREPARE stmt FROM @sql;
	EXECUTE stmt;
	DEALLOCATE PREPARE stmt;

	SET @sql := "INSERT INTO `tmp_matchOption` (";
	set varText := "(";
	OPEN varCursor1;
	read_loop: LOOP
		FETCH varCursor1 INTO varRank, varGold, varMedal, varExp;
		IF varIsDone=1 THEN
			LEAVE read_loop;
		END IF;

		SET @sql := CONCAT(@sql,"`Rank", varRank,"`,");
		SET varText := CONCAT(varText, varRank, ",");

		SET @sql := CONCAT(@sql,"`Gold", varRank,"`,");
		SET varText := CONCAT(varText, varGold, ",");

		SET @sql := CONCAT(@sql,"`Medal", varRank,"`,");
		SET varText := CONCAT(varText, varMedal, ",");
		
		IF varRank=varAwardCnt THEN
			SET @sql := CONCAT(@sql,"`Exp", varRank,"`");
			SET varText := CONCAT(varText, varExp);
		ELSE
			SET @sql := CONCAT(@sql,"`Exp", varRank,"`,");
			SET varText := CONCAT(varText, varExp, ",");
		END IF;

	END LOOP;
	CLOSE varCursor1;

	SET @sql := CONCAT(@sql,")");
	SET varText := CONCAT(varText,")");
	SET @sql := CONCAT(@sql, " VALUES ", varText);

	PREPARE stmt1 FROM @sql;
	EXECUTE stmt1;
	DEALLOCATE PREPARE stmt1;

	SET @sql := NULL;

	SELECT *, varServerID AS "ServerID", varMatchName AS "MatchName", varMatchStartHour AS "MatchStartHour", varMatchStartMinute AS "MatchStartMinute", varMatchEndHour AS "MatchEndHour", varMatchEndMinute AS "MatchEndMinute", varFirstMatchHour AS "FirstMatchHour", varFirstMatchMinute AS "FirstMatchMinute", varMatchDuration AS "MatchDuration", varMatchInterval AS "MatchInterval", varMatchFee AS "MatchFee", varMatchInitScore AS "MatchInitScore", varMatchAwardMinScore AS "MatchAwardMinScore", varMatchInitTable AS "MatchInitTable" FROM `tmp_matchOption`;
	
	DROP TEMPORARY TABLE IF EXISTS `tmp_matchOption`;
END
